// ラベルの色を取得するヘルパー関数
export const getLabelColor = (label: string): string => {
  // 26色のカラーパレット（視覚的に区別しやすい色を選択）
  const colors = [
    '#dc2626', // A: 赤
    '#2563eb', // B: 青
    '#16a34a', // C: 緑
    '#9333ea', // D: 紫
    '#ea580c', // E: オレンジ
    '#0891b2', // F: シアン
    '#c026d3', // G: マゼンタ
    '#84cc16', // H: ライム
    '#f97316', // I: 明るいオレンジ
    '#06b6d4', // J: 明るいシアン
    '#a855f7', // K: 明るい紫
    '#65a30d', // L: オリーブ
    '#e11d48', // M: ローズ
    '#0ea5e9', // N: スカイブルー
    '#10b981', // O: エメラルド
    '#f59e0b', // P: アンバー
    '#8b5cf6', // Q: バイオレット
    '#14b8a6', // R: ティール
    '#f43f5e', // S: レッド・ピンク
    '#3b82f6', // T: ブルー
    '#22c55e', // U: グリーン
    '#a78bfa', // V: ラベンダー
    '#facc15', // W: イエロー
    '#fb923c', // X: 薄オレンジ
    '#e879f9', // Y: フクシア
    '#38bdf8', // Z: ライトブルー
  ]
  
  const index = label.charCodeAt(0) - 65 // A=0, B=1, ..., Z=25
  if (index >= 0 && index < 26) {
    return colors[index]
  }
  return '#333' // デフォルト
}